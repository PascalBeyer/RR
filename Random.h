static u32 xorshift32(RandomSeries *series)
{
	u32 x32 = series->value;
	x32 ^= x32 << 13;
	x32 ^= x32 >> 17;
	x32 ^= x32 << 5;
	series->value = x32;
	return x32;
}

static u32 RandomU32(RandomSeries *series)
{
	return xorshift32(series);
}

static u32 RandomColorU32(RandomSeries *series)
{
	u32 r = xorshift32(series);
	u32 c = 0xFF000000 | r >> 8;
   
	return c;
}

static f32 RandomPercent(RandomSeries *series)
{
	return (float)xorshift32(series) / (float)MAXU32;
}

static f32 RandomSignedPercent(RandomSeries *series)
{
	return (2 * RandomPercent(series) - 1);
}

inline float RandomGaussian(RandomSeries *r)
{	
	f32 x, y;
   
	while (true) 
	{
		u32 U = xorshift32(r);
		u32 i = U & 0x7F;		/* 7 bit to choose the step */
		u32 sign = U & 0x80;	/* 1 bit for the sign */
		u32 j = U >> 8;			/* 24 bit for the x-value */
		
		x = j*wtab[i];
		if (j < ktab[i])
		{
			return  (sign ? (x) : (-x));
		}
      
		if (i < 127u) 
		{			
			f32 y0 = ytab[i];
			f32 y1 = ytab[i + 1];
			y = y1 + (y0 - y1)*RandomPercent(r);
		}
		else 
		{
			x = PARAM_R - logf(1.0f - RandomPercent(r)) / PARAM_R;
			y = expf(-PARAM_R*(x - 0.5f*PARAM_R))*RandomPercent(r);
		}
		if (y < expf(-0.5f*x*x))
		{
			return  (sign ? (x) : (-x));
		}
	}
   
}

inline float RandomPositiveGaussian(RandomSeries *r)
{
	f32 x, y;
   
	while (true)
	{
		u32 U = xorshift32(r);
		u32 i = U & 0x7F;		/* 7 bit to choose the step */
		u32 j = U >> 8;			/* 24 bit for the x-value */
      
		x = j*wtab[i];
		if (j < ktab[i])
		{
			return  x;
		}
      
		if (i < 127u)
		{
			f32 y0 = ytab[i];
			f32 y1 = ytab[i + 1];
			y = y1 + (y0 - y1)*RandomPercent(r);
		}
		else
		{
			x = PARAM_R - logf(1.0f - RandomPercent(r)) / PARAM_R;
			y = expf(-PARAM_R*(x - 0.5f*PARAM_R))*RandomPercent(r);
		}
		if (y < expf(-0.5f*x*x))
		{
			return  x;
		}
	}
}

